package tandem;

public class PlantDetails {

	String name;
	String water_after;
	
	public PlantDetails( ) {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getWater_after() {
		return water_after;
	}

	public void setWater_after(String water_after) {
		this.water_after = water_after;
	}

	

}
